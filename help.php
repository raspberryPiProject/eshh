
<h2>Bedienungsanleitung / Hilfe</h2>
<p class = "header2">Erste Inbetriebnahme</p>
<p class = "header">1. Futter auff&uuml;llen</p>
<p>Vor der ersten Inbetriebnahme, muss das Futter aufgef&uuml;llt werden.</p>
<p class = "header">2. Futterstand einstellen</p>
<p>Nachdem Sie das Futter Ihrer Wahl in den Beh&auml;lter gef&uuml;llt haben, muss der Futterstand angepasst werden (siehe Abschnitt <a href = "#foodFill" data-ajax="false">"Futterstand"</a>).</p>
<p class = "header">3.Futtermenge abmessen/einstellen</p>
<p>Um die Futterration zu bestimmen und einzustellen, muss diese zuerst in mehreren Durchg채ngen ermittelt werden. Benutzen Sie dazu die Funktion "Direkt-Futterausgabe" (siehe Abschnitt <a href="#directFood" data-ajax="false">"Direkt-Futterausgabe"</a>). <br>
Nachdem das Futter ausgegeben wurde, wiegen Sie das Gewicht ab. Wiederholen Sie diesen Vorgang einige Male, notieren Sie sich das Gewicht und berechnen Sie schliesslich den Durchschnitt.
Dieses Durchschnittsgewicht muss nun wie unter Punkt <a href="#foodAmount">"Einheit Futterausgabe"</a> beschrieben eingestellt werden. </p>
<p class = "header">4. F&uuml;tterungs-Zeitplan erstellen</p>
<p>Damit der Futterautomat nun automatisch Futter ausgibt, muss wie unter Punkt <a href="#foodSchedule" data-ajax="false">"F&uuml;tterungszeitplan"</a> der F&uuml;tterungszeitplan eingestellt werden.
<p class = "header">5. Email-Notifikation einstellen</hp>
<p>Bei niedrigem Futterstand wird automatisch eine Info-Mail verschickt. Es muss deshalb gem채ss Punkt <a href = "#notif" data-ajax="false">"allgemeine Einstellungen"</a> die Email-Adresse hinterlegt werden</p>
<p class = "header">6. Bewegungsmelder (optional)</p>
<p>Falls der Bewegungsmelder aktiviert werden soll, muss dies unter Punkt <a href="#movementStat" data-ajax="false">"Statistik Bewegungsmelder"</a> zuerst aktiviert werden.</p>
<p>Damit ist der Futterautomat nun startklar.</p>

<p class = "header2"><a name="foodSchedule" data-ajax="false">F&uuml;tterungszeitplan</a></p>
<p>Auf der Startseite unter Einstellungen/F&uuml;tterungszeitplan kann der Zeitplan eingestellt werden. Es &ouml;ffnet sich ein Formular, wo insgesamt 10 F&uuml;tterungszeiten eingestellt werden k&ouml;nnen.
Dazu wird unter "Zeit" die Uhrzeit mit Anw&auml;hlen des Uhrensymbols eine Uhrzeit gew&auml;hlt. Daneben wird die gew&uuml;nschte Futtermenge in Gramm angegeben. Sind alle F&uuml;tterungszeiten eingestellt, 
k&ouml;nnen die Einstellungen mit "Speichern" gespeichert werden.<br>
Leer gelassene Zeilen werden ignoriert.</p>

<p class = "header2"><a name="foodAmount" data-ajax="false">Einheit Futterausgabe</a></p>
<p>Da die Futtermenge je nach Futterart variieren kann, muss dem System mitgeteilt werden, wieviel Gramm Futter pro Umdrehung ausgegeben wird. Dies kann unter "Einstellungen/Einheit Futterausgabe" gemacht werden. 
Hierbei wird f체r eine Umdrehung die Anzahl Gramm Futter eingegeben.</p>

<p class = "header2"><a name="foodFill" data-ajax="false">Futterstand</a></p>
<p>Nach jeder Auff&uuml;llung des Futters muss der aktuelle Futterstand angegeben werden. Dazu wird unter "Einstellungen/Futterstand" sowohl der F&uuml;llstand in % wie auch in Gramm angegeben. </p>

<p class = "header2"><a name="notif" data-ajax="false">allgemeine Einstellungen</a></p>
<p>Hier kann die Email-Adresse, die f&uuml;r das Versenden von St&ouml;rungsmeldungen verwendet wird, angegeben werden.</p>

<p class="header2"><a name="movement">Bewegungsmelder</a></p>
<p> Um den Bewegungsmelder zu starten, muss zun&auml;chst unter "Bewegungsmelder/Bewegungsmelder steuern" 
<p class = "header2"><a name="movementStat">Statistik Bewegungsmelder</a> </p>
<p>Um zu &uuml;berpr&uuml;fen, wann sich die Tiere vor dem Futterautomat aufgehalten haben, kann unter "Statistik Bewegungsmelder" pro gew&uuml;nschtem Datum angeschaut werden, um welche Uhrzeit der Bewegungsmelder Bewegungen 
registriert hat.</p>

<p class = "header2"><a name="directFood" data-ajax="false">Direkt-Futterausgabe</a></p>
<p>Um unabh&auml;ngig des Zeitplans eine zus&auml;tzliche Futterration auszugeben, kann dies mittels Anw채hlen der Option "Direkt-Futterausgabe" auf der Startseite gemacht werden.</p>


